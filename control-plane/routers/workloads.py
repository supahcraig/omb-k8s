"""Workload library CRUD router — /api/workloads."""
import logging
from typing import Dict, List
from uuid import uuid4

import yaml
from fastapi import APIRouter, Depends, HTTPException
from sqlalchemy import select
from sqlalchemy.ext.asyncio import AsyncSession

from database import get_db
from models import Workload
from schemas import WorkloadCreate, WorkloadOut, WorkloadUpdate

logger = logging.getLogger(__name__)

router = APIRouter()


# ---------------------------------------------------------------------------
# Internal helpers
# ---------------------------------------------------------------------------


def _auto_description(content: str) -> str | None:
    """Parse YAML workload content and return a brief description string.

    Extracts messageSize, partitionsPerTopic, and producerRate fields if
    present.  Returns None when no relevant fields are found or when the
    content cannot be parsed.
    """
    try:
        data = yaml.safe_load(content)
    except yaml.YAMLError:
        return None
    if not isinstance(data, dict):
        return None

    parts: List[str] = []
    if data.get("messageSize"):
        parts.append(f"{data['messageSize'] // 1024}KB messages")
    if data.get("partitionsPerTopic"):
        parts.append(f"{data['partitionsPerTopic']} partitions")
    if data.get("producerRate"):
        rate = data["producerRate"]
        parts.append(f"{rate:,} msg/s target")

    return ", ".join(parts) if parts else None


